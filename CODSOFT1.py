#task:Calculator
a=float(input('Enter 1st number: '))
b=float(input('Enter 2nd number: '))
choice=input("Enter the operation as 'A' for addition,'S' for subtraction,'M' for multiplication,'D' for division,'Mo' for modulus: ")
match(choice):
    case 'A':
        print('Addition',a+b)
    case 'S':
        print('Subtraction',a-b)
    case 'M':
        print('Multiplication',a*b)
    case 'D':
        if b!=0:
            print('Division',a/b)
        else:
            print('divided by zero')
    case 'Mo':
        print('Modulus',a%b)
    case _:
        print('Invalid operation')
print('end')
